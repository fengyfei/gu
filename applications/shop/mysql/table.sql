CREATE DATABASE IF NOT EXISTS `shop`;
USE `shop`;

CREATE TABLE IF NOT EXISTS `users` (
  `id` INT(64) UNSIGNED NOT NULL AUTO_INCREMENT,
  `unionid` VARCHAR(29)  UNIQUE,
  `username` VARCHAR(128),
  `phone` VARCHAR (16),
  `password` VARCHAR(128),
  `sex` INT(8) UNSIGNED,
  `isadmin` BOOLEAN NOT NULL,
  `type` VARCHAR(32),
  `created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `lastlogin` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `isactive` BOOLEAN DEFAULT TRUE,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE IF NOT EXISTS `phoneuser` (
  `id` INT(64) UNSIGNED NOT NULL AUTO_INCREMENT,
  `username` VARCHAR(128),
  `phone` VARCHAR (16),
  `password` VARCHAR(128),
  `avatar` VARCHAR(128),
  `sex` INT(8) UNSIGNED,
  `isadmin` BOOLEAN NOT NULL,
  `created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE IF NOT EXISTS `address` (
  `id` INT(64) UNSIGNED NOT NULL AUTO_INCREMENT,
  `userid` INT(32) UNSIGNED NOT NULL,
  `name` VARCHAR(32) NOT NULL,
  `phone` VARCHAR (16) NOT NULL,
  `address` VARCHAR(128) NOT NULL,
  `isdefault` BOOLEAN NOT NULL DEFAULT FALSE,
  `created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE IF NOT EXISTS `cart` (
  `id` INT(64) UNSIGNED NOT NULL AUTO_INCREMENT,
  `userid` INT(64) UNSIGNED NOT NULL,
  `wareid` INT(64) UNSIGNED,
  `count` INT(32) UNSIGNED NOT NULL,
  `created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE IF NOT EXISTS `category` (
  `id` INT(64) UNSIGNED NOT NULL AUTO_INCREMENT,
  `category` VARCHAR (50) NOT NULL,
  `description` VARCHAR (100) NOT NULL,
  `parentid` INT(64) UNSIGNED NOT NULL DEFAULT 0,
  `status` INT(8) UNSIGNED NOT NULL DEFAULT 1,
  `created` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB AUTO_INCREMENT=1 DEFAULT CHARSET=utf8 COLLATE=utf8_bin;
